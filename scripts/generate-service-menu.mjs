import { readFileSync, writeFileSync, mkdirSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const tsvPath = join(__dirname, "service-menu.tsv");
const raw = readFileSync(tsvPath, "utf8").trim();
const lines = raw ? raw.split(/\r?\n/) : [];

function trimTrailingPeriod(s) {
  return s.replace(/\s*\.\s*$/u, "").trimEnd();
}

function parseRow(line) {
  const [serviceName, avgTime, cost, costUnit, category, audience] = line.split("\t");
  return {
    serviceName: trimTrailingPeriod(serviceName),
    avgTimeMinutes: Number(avgTime),
    costInr: Number(cost),
    costUnit: costUnit === "Fixed" ? "Fixed" : "From",
    category: trimTrailingPeriod(category),
    audience: trimTrailingPeriod(audience),
  };
}

const items = lines.map(parseRow);
const categories = [...new Set(items.map((i) => i.category))].sort((a, b) =>
  a.localeCompare(b, undefined, { sensitivity: "base" }),
);

function esc(s) {
  return s.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

const itemsTs = items
  .map(
    (i) =>
      `  {\n    serviceName: "${esc(i.serviceName)}",\n    avgTimeMinutes: ${i.avgTimeMinutes},\n    costInr: ${i.costInr},\n    costUnit: "${i.costUnit}",\n    category: "${esc(i.category)}",\n    audience: "${esc(i.audience)}",\n  }`,
  )
  .join(",\n");

const categoriesTs = categories.map((c) => `  "${esc(c)}",`).join("\n");

const out = `// Generated by scripts/generate-service-menu.mjs — do not edit by hand.

export type ServiceCostUnit = "From" | "Fixed";

export type ServiceAudience = "Unisex" | "Women" | "Men";

export type ServiceMenuItem = {
  serviceName: string;
  avgTimeMinutes: number;
  costInr: number;
  costUnit: ServiceCostUnit;
  category: string;
  audience: ServiceAudience;
};

export const serviceMenuItems: ServiceMenuItem[] = [
${itemsTs},
];

export const serviceMenuCategories: string[] = [
${categoriesTs}
];

export type ServiceMenuCategoryGroup = {
  category: string;
  items: ServiceMenuItem[];
};

export function groupServiceMenuByCategory(
  menu: ServiceMenuItem[] = serviceMenuItems,
): ServiceMenuCategoryGroup[] {
  const byCategory = new Map<string, ServiceMenuItem[]>();
  for (const item of menu) {
    const list = byCategory.get(item.category) ?? [];
    list.push(item);
    byCategory.set(item.category, list);
  }
  return [...byCategory.entries()]
    .sort(([a], [b]) => a.localeCompare(b, undefined, { sensitivity: "base" }))
    .map(([category, categoryItems]) => ({ category, items: categoryItems }));
}

const inrFormatter = new Intl.NumberFormat("en-IN", {
  style: "currency",
  currency: "INR",
  maximumFractionDigits: 0,
});

export function formatServicePrice(item: Pick<ServiceMenuItem, "costInr" | "costUnit">): string {
  const amount = inrFormatter.format(item.costInr);
  return item.costUnit === "From" ? amount + " From" : amount;
}

export function formatServiceDuration(minutes: number): string {
  if (minutes < 60) return "~" + minutes + " min";
  const hrs = Math.floor(minutes / 60);
  const mins = minutes % 60;
  if (mins === 0) return "~" + hrs + " hr";
  return "~" + hrs + " hr " + mins + " min";
}
`;

const outPath = join(__dirname, "..", "src", "data", "serviceMenu.ts");
mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, out, "utf8");
console.log("items", items.length);
console.log("categories", categories.length);
